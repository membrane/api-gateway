package com.predic8.membrane.annot.util;

import com.predic8.membrane.annot.Grammar;
import com.predic8.membrane.annot.yaml.*;

import java.io.IOException;
import java.lang.reflect.InvocationTargetException;
import java.util.concurrent.CountDownLatch;

import static java.util.Objects.requireNonNull;

public class YamlParser {
    private final BeanRegistry beanRegistry;

    public YamlParser(String resourceName) throws ClassNotFoundException, NoSuchMethodException, InvocationTargetException, InstantiationException, IllegalAccessException, IOException, InterruptedException, YamlSchemaValidationException {
        Grammar generator = (Grammar) getClass().getClassLoader()
                .loadClass("com.predic8.membrane.demo.config.spring.GrammarAutoGenerated")
                .getConstructor()
                .newInstance();

        CountDownLatch cdl = new CountDownLatch(1);
        beanRegistry = GenericYamlParser.parseMembraneResources(
                requireNonNull(getClass().getResourceAsStream(resourceName)), generator,
                new BeanCacheObserver() {
                    @Override
                    public void handleAsynchronousInitializationResult(boolean empty) {
                        cdl.countDown();
                    }

                    @Override
                    public void handleBeanEvent(BeanDefinition bd, Object bean, Object oldBean) throws IOException {

                    }

                    @Override
                    public boolean isActivatable(BeanDefinition bd) {
                        return true;
                    }
                });
        cdl.await();
    }

    public BeanRegistry getBeanRegistry() {
        return beanRegistry;
    }
}
