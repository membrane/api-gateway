    private JsonRPCProtectionInterceptor interceptor(String config) throws Exception {
        JsonRPCProtectionInterceptor interceptor = parseInterceptor(config);
        interceptor.init(new DefaultRouter());
        return interceptor;
    }

    private JsonRPCProtectionInterceptor parseInterceptor(String config) throws Exception {
        JsonNode node = YAML.readTree(wrapConfig(config));
        var grammar = new GrammarAutoGenerated();
        var registry = new BeanRegistryImplementation(grammar);
        return GenericYamlParser.createAndPopulateNode(
                new ParsingContext<>("jsonRPCProtection", registry, grammar, node, "$.jsonRPCProtection", null),
                JsonRPCProtectionInterceptor.class,
                node.get("jsonRPCProtection")
        );
    }

    private String wrapConfig(String config) {
        if (config == null || config.isBlank()) {
            return "jsonRPCProtection: {}\n";
        }
        return "jsonRPCProtection:\n" + config.stripIndent().indent(2);
    }

    private Exchange exchange(String method, String contentType, String body) {
        Request.Builder builder = new Request.Builder()
                .method(method)
                .uri("/");

        if (contentType != null) {
            builder.contentType(contentType);
        }
        if (body != null) {
            builder.body(body);
        }

        return builder.buildExchange();
    }

    private Response jsonResponse(String body) {
        return Response.ok()
                .json(body)
                .build();
    }

    private void assertValidationError(Response response,
                                       int expectedStatus,
                                       int expectedJsonRpcCode,
                                       String expectedMessageSnippet,
                                       Object expectedId,
                                       boolean batchShape) throws Exception {
        assertNotNull(response);
        assertEquals(expectedStatus, response.getStatusCode());

        JsonNode root = OM.readTree(response.getBodyAsStringDecoded());
        if (batchShape) {
            assertTrue(root.isArray());
            assertEquals(1, root.size());
            root = root.get(0);
        } else {
            assertTrue(root.isObject());
        }

        assertEquals("2.0", root.path("jsonrpc").asText());
        assertEquals(expectedJsonRpcCode, root.path("error").path("code").asInt());
        assertTrue(root.path("error").path("message").asText().contains(expectedMessageSnippet));

        JsonNode idNode = root.get("id");
        assertNotNull(idNode);
        if (expectedId == null) {
            assertTrue(idNode.isNull());
        } else {
            assertEquals(OM.valueToTree(expectedId), idNode);
        }
    }

    private static boolean payloadIsBatch(String payload) {
        return payload != null && payload.trim().startsWith("[");
    }

    private boolean containsMessage(Throwable throwable, String expectedMessageSnippet) {
        for (Throwable current = throwable; current != null; current = current.getCause()) {
            if (current.getMessage() != null && current.getMessage().contains(expectedMessageSnippet)) {
                return true;
            }
        }
        return false;
    }

    private record RequestCase(String name,
                               String config,
                               String method,
                               String contentType,
                               String body,
                               Outcome expectedOutcome,
                               Integer expectedStatus,
                               Integer expectedJsonRpcCode,
                               String expectedMessageSnippet,
                               Object expectedId,
                               boolean batchErrorShape) {
        private boolean expectsRejection() {
            return expectedOutcome == RETURN;
        }

        @Override
        public String toString() {
            return name;
        }
    }

