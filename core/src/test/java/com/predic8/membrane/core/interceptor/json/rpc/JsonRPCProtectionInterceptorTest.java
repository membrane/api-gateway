    @ParameterizedTest(name = "{0}")
    @MethodSource("requestCases")
    void validatesRequests(RequestCase testCase) throws Exception {
        var interceptor = interceptor(testCase.config());
        var exc = exchange(testCase.method(), testCase.contentType(), testCase.body());

        assertEquals(testCase.expectedOutcome(), interceptor.handleRequest(exc));

        if (testCase.expectsRejection()) {
            assertValidationError(
                    exc.getResponse(),
                    testCase.expectedStatus(),
                    testCase.expectedJsonRpcCode(),
                    testCase.expectedMessageSnippet(),
                    testCase.expectedId(),
                    testCase.batchErrorShape()
            );
            return;
        }

        assertNull(exc.getResponse());
    }

    @ParameterizedTest(name = "{0}")
    @MethodSource("responseCases")
    void validatesResponses(ResponseCase testCase) throws Exception {
        var interceptor = interceptor(testCase.config());
        var exc = exchange(METHOD_POST, APPLICATION_JSON, testCase.requestBody());

        if (testCase.requestBody() != null) {
            assertEquals(CONTINUE, interceptor.handleRequest(exc), "response test setup must pass request validation");
        }

        exc.setResponse(jsonResponse(testCase.responseBody()));
        assertEquals(testCase.expectedOutcome(), interceptor.handleResponse(exc));

        if (testCase.expectsRejection()) {
            assertValidationError(
                    exc.getResponse(),
                    testCase.expectedStatus(),
                    testCase.expectedJsonRpcCode(),
                    testCase.expectedMessageSnippet(),
                    testCase.expectedId(),
                    testCase.batchErrorShape()
            );
            return;
        }

        assertNotNull(exc.getResponse());
        assertTrue(exc.getResponse().getBodyAsStringDecoded().contains("\"jsonrpc\""));
    }

    @Test
    void parsesSchemaValidationConfigFromYaml() throws Exception {
        var interceptor = parseInterceptor("""
                schemaValidation:
                  error:
                    schema:
                      type: object
                      required:
                        - code
                      properties:
                        code:
                          type: integer
                  methods:
                    'rpc.echo':
                      params:
                        location: classpath:/json/rpc/echo-params.schema.json
                      response:
                        schema:
                          type: object
                          properties:
                            message:
                              type: string
                    'rpc.health':
                      response:
                        location: classpath:/json/rpc/generic-rpc-params.schema.json
                """);

        JsonRPCSchemaValidation schemaValidation = interceptor.getSchemaValidation();
        assertNotNull(schemaValidation);
        assertNotNull(schemaValidation.getErrorValidation());
        assertEquals(
                "object",
                schemaValidation.getErrorValidation().getSchema().getProperties().get("type")
        );

        Map<String, JsonRPCMethodSchemas> methods = schemaValidation.getMethods().getMethods();
        assertEquals(
                "classpath:/json/rpc/echo-params.schema.json",
                methods.get("rpc.echo").getParams().getLocation()
        );
        assertEquals(
                "string",
                ((Map<?, ?>) ((Map<?, ?>) methods.get("rpc.echo").getResponse().getSchema().getProperties().get("properties")).get("message")).get("type")
        );
        assertEquals(
                "classpath:/json/rpc/generic-rpc-params.schema.json",
                methods.get("rpc.health").getResponse().getLocation()
        );
    }

